package ExerPOO;

public class Exer05_Patinete {

	String marca;
	String material;
	int tempoDeUso;
	String tipoDeRoda;
	Double PesoLimite;
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial(String material) {
		this.material = material;
	}
	public int getTempoDeUso() {
		return tempoDeUso;
	}
	public void setTempoDeUso(int tempoDeUso) {
		this.tempoDeUso = tempoDeUso;
	}
	public String getTipoDeRoda() {
		return tipoDeRoda;
	}
	public void setTipoDeRoda(String tipoDeRoda) {
		this.tipoDeRoda = tipoDeRoda;
	}
	public Double getPesoLimite() {
		return PesoLimite;
	}
	public void setPesoLimite(Double pesoLimite) {
		PesoLimite = pesoLimite;
	}
	
	
}
